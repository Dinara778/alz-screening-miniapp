export type DomainInterpretationName =
  | 'memory'
  | 'attention'
  | 'speed'
  | 'stability'
  | 'flexibility';

type ScoreTierId = 't81' | 't61' | 't41' | 't21' | 't0';

const TIER_ORDER: ScoreTierId[] = ['t81', 't61', 't41', 't21', 't0'];

function resolveScoreTier(score: number): ScoreTierId {
  const s = Math.max(0, Math.min(100, Math.round(Number.isFinite(score) ? score : 0)));
  if (s >= 81) return 't81';
  if (s >= 61) return 't61';
  if (s >= 41) return 't41';
  if (s >= 21) return 't21';
  return 't0';
}

function toPresentMomentText(text: string): string {
  if (/^(Сейчас|Прямо сейчас|Скорее всего)/i.test(text)) return text;
  return `Сейчас ${text.charAt(0).toLowerCase()}${text.slice(1)}`;
}

function toCopy(tier: DomainInterpretationTier): DomainInterpretationCopy {
  return {
    inLife: toPresentMomentText(tier.inLife),
    manifestations: toPresentMomentText(tier.manifestation),
    aboutResult: tier.insight,
  };
}

