function normalizeFree(copy: FreeIndexInterpretation): FreeIndexInterpretation {
  return {
    inLife: normalizeInterpretationFragment(copy.inLife),
    feeling: normalizeInterpretationFragment(copy.feeling),
    insight: normalizeInterpretationFragment(copy.insight),
  };
}

/** Бесплатная развёрнутая интерпретация по диапазону индекса (8 категорий). */
export function getFreeIndexInterpretation(index: number): FreeIndexInterpretation {
  if (!Number.isFinite(index)) return normalizeFree(INSUFFICIENT);

  const v = Math.max(0, Math.min(100, Math.round(index)));

  if (v >= 90) return normalizeFree(BAND_90_100);
  if (v >= 80) return normalizeFree(BAND_80_89);
  if (v >= 70) return normalizeFree(BAND_70_79);
  if (v >= 60) return normalizeFree(BAND_60_69);
  if (v >= 50) return normalizeFree(BAND_50_59);
  if (v >= 40) return normalizeFree(BAND_40_49);
  if (v >= 25) return normalizeFree(BAND_25_39);
  return normalizeFree(BAND_0_24);
}
