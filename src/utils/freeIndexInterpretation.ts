/** Бесплатная развёрнутая интерпретация по диапазону индекса (8 категорий). */
export function getFreeIndexInterpretation(index: number): FreeIndexInterpretation {
  if (!Number.isFinite(index)) return { ...INSUFFICIENT };

  const v = Math.max(0, Math.min(100, Math.round(index)));

  if (v >= 90) return BAND_90_100;
  if (v >= 80) return BAND_80_89;
  if (v >= 70) return BAND_70_79;
  if (v >= 60) return BAND_60_69;
  if (v >= 50) return BAND_50_59;
  if (v >= 40) return BAND_40_49;
  if (v >= 25) return BAND_25_39;
  return BAND_0_24;
}
