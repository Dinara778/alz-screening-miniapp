const DEFICIT_RECOMMENDATION_ORDER: LeadingDeficit[] = [
  'memory',
  'attention',
  'stability',
  'speed',
  'flexibility',
];

function isOverloadZoneId(id: string): id is OverloadZoneId {
  return (OVERLOAD_DISPLAY_ORDER as readonly string[]).includes(id);
}

/** Активные зоны перегрузки с дневными текстами (только active: true). */
export function getOverloadMapWithTemporalTexts(overloadMap: OverloadMapItem[]): TemporalOverloadCard[] {
  const activeIds = new Set(overloadMap.filter((row) => row.active).map((row) => row.id));

  return OVERLOAD_DISPLAY_ORDER.filter((id) => activeIds.has(id) && isOverloadZoneId(id)).map((id) => ({
    id,
    ...OVERLOAD_TEMPORAL_BY_ID[id],
  }));
}

/** Адресные рекомендации по ведущему дефициту; при ничьей по баллам — объединение без дублей. */
export function getTemporalRecommendations(
  _leadingDeficit: LeadingDeficit | null,
  domainScores: DomainScoresInput,
): string[] {
  const deficits = getLeadingDeficits(domainScores);
  if (!deficits.length) return [];

  const seen = new Set<string>();
  const merged: string[] = [];

  for (const deficit of DEFICIT_RECOMMENDATION_ORDER) {
    if (!deficits.includes(deficit)) continue;
    for (const line of TEMPORAL_RECOMMENDATIONS_BY_DEFICIT[deficit]) {
      if (seen.has(line)) continue;
      seen.add(line);
      merged.push(line);
    }
  }

  return merged;
}
